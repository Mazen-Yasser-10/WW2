    <h1>Soviet Union in World War II</h1>

    <h2>Prelude to War (1939-1941)</h2>
    <ul class="timeline">
        <li><strong>August 1939:</strong> Molotov-Ribbentrop Pact signed with Nazi Germany</li>
        <li><strong>September 1939:</strong> Invades Poland from the east after German invasion</li>
        <li><strong>1939-1940:</strong> Winter War with Finland</li>
        <li><strong>1940:</strong> Annexes Baltic states (Estonia, Latvia, Lithuania)</li>
    </ul>

    <h2>German Invasion (1941-1942)</h2>
    <ul class="timeline">
        <li><strong>June 1941:</strong> Operation Barbarossa - Germany invades USSR</li>
        <li><strong>1941:</strong> Siege of Leningrad begins (lasts 872 days)</li>
        <li><strong>October 1941:</strong> Battle of Moscow - First major German defeat</li>
        <li><strong>1942:</strong> Germans reach Stalingrad and Caucasus</li>
    </ul>

    <h2>Turning the Tide (1942-1944)</h2>
    <ul class="timeline">
        <li><strong>November 1942:</strong> Soviet counteroffensive at Stalingrad</li>
        <li><strong>February 1943:</strong> German surrender at Stalingrad</li>
        <li><strong>July 1943:</strong> Battle of Kursk - Largest tank battle in history</li>
        <li><strong>1944:</strong> Operation Bagration destroys German Army Group Center</li>
    </ul>

    <h2>Final Victory (1944-1945)</h2>
    <ul class="timeline">
        <li><strong>1944:</strong> Soviet forces enter Eastern Europe</li>
        <li><strong>January 1945:</strong> Vistula-Oder Offensive - Advance into Germany</li>
        <li><strong>April-May 1945:</strong> Battle of Berlin</li>
        <li><strong>May 8, 1945:</strong> Germany surrenders to Soviet forces</li>
    </ul>

    <h2>Consequences</h2>
    <ul class="timeline">
        <li>Estimated 27 million Soviet deaths (military and civilian)</li>
        <li>Massive destruction of infrastructure and industry</li>
        <li>Establishment of Soviet control over Eastern Europe</li>
        <li>Emergence as a superpower rivaling the United States</li>
    </ul>
