    <h1>Germany in World War II </h1>

    <h2>Background and Prelude</h2>
    <ul class="timeline">
        <li><strong>1933:</strong> Adolf Hitler and the Nazi Party rise to power in Germany</li>
        <li><strong>1935:</strong> Germany begins rearmament in violation of the Treaty of Versailles</li>
        <li><strong>1938:</strong> Annexation of Austria (Anschluss) and occupation of Czechoslovakia</li>
    </ul>

    <h2>Early War (1939-1941)</h2>
    <ul class="timeline">
        <li><strong>September 1939:</strong> Invasion of Poland - Beginning of World War II</li>
        <li><strong>1940:</strong> Germany occupies Denmark, Norway, Netherlands, Belgium, Luxembourg, and France</li>
        <li><strong>Summer 1940:</strong> Battle of Britain - Germany fails to invade Britain</li>
        <li><strong>June 1941:</strong> Operation Barbarossa - Invasion of the Soviet Union</li>
    </ul>

    <h2>Peak and Turning Point (1941-1943)</h2>
    <ul class="timeline">
        <li><strong>December 1941:</strong> Germany declares war on the United States after Pearl Harbor</li>
        <li><strong>1942:</strong> Peak of German expansion in Europe and North Africa</li>
        <li><strong>February 1943:</strong> German defeat at Stalingrad - Major turning point in the war</li>
        <li><strong>July 1943:</strong> German defeat at the Battle of Kursk</li>
    </ul>

    <h2>Retreat and Defeat (1944-1945)</h2>
    <ul class="timeline">
        <li><strong>June 1944:</strong> D-Day - Allied invasion of Normandy opens Western Front</li>
        <li><strong>July 1944:</strong> Failed assassination attempt against Hitler (July 20 Plot)</li>
        <li><strong>January 1945:</strong> Soviet Red Army reaches German territory</li>
        <li><strong>April 1945:</strong> Soviet forces enter Berlin</li>
        <li><strong>April 30, 1945:</strong> Hitler commits suicide in his Berlin bunker</li>
        <li><strong>May 8, 1945:</strong> Germany's unconditional surrender - End of war in Europe</li>
    </ul>

    <h2>Consequences for Germany</h2>
    <ul class="timeline">
        <li>Germany divided into four occupation zones (American, British, French, Soviet)</li>
        <li>Most German cities and infrastructure destroyed</li>
        <li>Estimated 5-7 million German casualties</li>
        <li>Nuremberg Trials for Nazi war criminals</li>
        <li>Beginning of Cold War and division into East and West Germany</li>
    </ul>
